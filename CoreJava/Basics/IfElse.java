class IfElse
{
	public static void main(String args[])
	{
		int a=10,b=20;
		if(a>b)
		{
			System.out.println("a is greater ");
			System.out.println("Value of a is : "+a);
		}
		else
		{
			System.out.println("b is greater ");
			System.out.println("Value of a is : "+b);	
		}
	}

}